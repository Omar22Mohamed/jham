				<h2 class="top">What is the PESO?</h2>
				<p>The Public Employment Service Office (PESO) is a non-fee charging multi-employment service facility or entity established or accredited pursuant to Republic Act No. 8759, otherwise known as the PESO Act of 1999. The Act provides that in order to carry out full employment and equality of employment opportunities for all, and to strengthen and expand the existing employment facilitation service machinery of the government particularly at the local levels, there shall be established in all capital towns of provinces, key cities, and other strategic areas a Public Employment Service Office.</p>
				<p>The PESO's are community-based and maintained largely by local government units (LGU's) and a number of non-governmental organizations (NGO's) or community-based organizations (CBO's) and state universities and colleges (SUC's). The PESO's are linked to the regional offices of the Department of Labor and Employment (DOLE) for coordination and technical supervision, and to the DOLE central office, to constitute the national employment service network.</p>
				<h2>What are the objectives of the PESO?</h2>
				<h3>General Objective</h3>
				<ul>
					<li>Ensure the prompt, timely and efficient delivery of employment service and provision of information on the other DOLE programs.</li>
				</ul>
				<h3>Specific Objectives</h3>
				<ul>
					<li>Provide a venue where people could explore simultaneously various employment options and actually seek assistance they prefer.</li>
					<li>Serve as referral and information center for the various services and programs of DOLE and other government agencies present in the area.</li>
					<li>Provide clients with adequate information on employment and labor market situation in the area.</li>
					<li>Network with other PESO's within the region on employment for job exchange purposes.</li>
				</ul>
				
