<script>
    $(function(){
        $('#form_employer').submit(function(event) {
            event.preventDefault();

            $.ajax({
                url: '<?php echo site_url('Applicant/save_employer_info'); ?>',
                type: "post",
                data: $('#form_employer').serialize(),
                success: function(data){
                    if (data == 1) {
                        Swal.fire({
                          position: 'center',
                          type: 'success',
                          title: 'Successfuly Save',
                          showConfirmButton: false,
                        })
                        window.setTimeout(function() {
                             window.location.href = '/jham/index.php/Frontpage/Dashboard';
                        }, 2000);
                       
                    }else{
                        Swal.fire({
                          position: 'center',
                          title: 'Fail save',
                          animation: true,
                          customClass: {
                            popup: 'animated tada'
                          }
                        })
                    }
                }
            })
        });

        $('#emp_name').change(function(){
           var emp_name = $('#emp_name').val();
           if(emp_name != ''){
            $.ajax({
             url: "<?php echo site_url(); ?>/Applicant/checkName",
             method: "POST",
             data: {emp_name:emp_name},
             success: function(data){
              if (data == 1) {
                $('#submit_emp').prop('disabled', true);
                $('.emp_check').html('<label class="text-danger"><span><i class="fa fa-times" aria-hidden="true"></i> This Name is already taken</span></label>');
              }else{
                $('#signup_submit').prop('disabled', false);
                $('.emp_check').html('<label class="text-success"><span><i class="fa fa-check-circle-o" aria-hidden="true"></i> Name is available</span></label>');
              }
             }
            });
           }
        });
    });
 </script>

